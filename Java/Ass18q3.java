////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Print Odd Numbers Demo
// Input        : Integer number
// Output       : All odd numbers up to given number
// Methods      : printOddNumbers(), System.out.println()
// Description  : Prints all odd numbers from 1 to given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printOddNumbers(int iNo)
    {
        for(int i = 1; i <= iNo; i += 2)
        {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates printing odd numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A18q3
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.printOddNumbers(10);
    }
}
