// Program      : Sum of Digits Demo
// Input        : Integer number
// Output       : Sum of digits of the number
// Methods      : sumOfDigits(), System.out.println()
// Description  : Calculates and prints sum of digits of a given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void sumOfDigits(int num)
    {
        int sum = 0;
        while(num > 0)
        {
            sum += num % 10;
            num /= 10;
        }
        System.out.println("Sum of digits: " + sum);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates sum of digits calculation.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A17q1
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.sumOfDigits(1234);
    }
}
