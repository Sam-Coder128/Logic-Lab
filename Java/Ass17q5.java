// Program      : Multiplication Table Demo
// Input        : Integer number
// Output       : Multiplication table of the number
// Methods      : printTable(), System.out.println()
// Description  : Prints multiplication table of a given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printTable(int num)
    {
        for(int i = 1; i <= 10; i++)
        {
            System.out.println(num + " x " + i + " = " + (num * i));
        }
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates multiplication table printing.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A17q5
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.printTable(5);
    }
}
