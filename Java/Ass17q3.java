// Program      : Maximum of Two Numbers Demo
// Input        : Two integers
// Output       : Largest of the two numbers
// Methods      : findMax(), System.out.println()
// Description  : Finds maximum of two given numbers
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void findMax(int a, int b)
    {
        if(a > b)
            System.out.println("Maximum is: " + a);
        else
            System.out.println("Maximum is: " + b);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates maximum of two numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A17q3
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.findMax(10, 20);
    }
}
