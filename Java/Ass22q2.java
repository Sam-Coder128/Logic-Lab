class Logic
{
    void subtractNumbers(int a, int b)
    {
        int diff = a - b;
        System.out.println("Subtraction : " + diff);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates subtraction of two numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A22q2
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.subtractNumbers(10, 20);
    }
}
