////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Minimum of Three Numbers Demo
// Input        : Three integers
// Output       : Smallest of the three numbers
// Methods      : findMin(), System.out.println()
// Description  : Finds minimum of three given numbers
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void findMin(int a, int b, int c)
    {
        int min = a;
        if(b < min) min = b;
        if(c < min) min = c;
        System.out.println("Minimum is: " + min);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates minimum of three numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A17q4
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.findMin(10, 20, 30);
    }
}
