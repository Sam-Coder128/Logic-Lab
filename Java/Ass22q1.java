class Logic
{
    void addNumbers(int a, int b)
    {
        int sum = a + b;
        System.out.println("Addition : " + sum);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates addition of two numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A22q1
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.addNumbers(10, 20);
    }
}
