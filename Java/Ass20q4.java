class Logic
{
    void findLargestDigit(int iNo)
    {
        int iDigit = 0;
        int iMax = 0;

        while(iNo > 0)
        {
            iDigit = iNo % 10;

            if(iDigit > iMax)
            {
                iMax = iDigit;
            }

            iNo = iNo / 10;
        }

        System.out.println("Largest digit : " + iMax);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates finding largest digit.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A20q4
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.findLargestDigit(83429);
    }
}
