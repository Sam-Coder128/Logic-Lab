// Program      : Print Digits Demo
// Input        : Integer number
// Output       : Digits of the number
// Methods      : printDigits(), System.out.println()
// Description  : Prints digits of a given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printDigits(int iNo)
    {
        while(iNo > 0)
        {
            int digit = iNo % 10;
            System.out.println(digit);
            iNo /= 10;
        }
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates printing digits of a number.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A19q4
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.printDigits(9876);
    }
}
