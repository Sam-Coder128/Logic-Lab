////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Print Even Numbers Demo
// Input        : Integer number
// Output       : All even numbers up to given number
// Methods      : printEvenNumbers(), System.out.println()
// Description  : Prints all even numbers from 1 to given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printEvenNumbers(int iNo)
    {
        for(int i = 2; i <= iNo; i += 2)
        {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates printing even numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A18q2
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.printEvenNumbers(10);
    }
}
