// Program      : Product of Digits Demo
// Input        : 234
// Output       : Product : 24
// Description  : Calculates product of digits of a given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void productDigits(int iNo)
    {
        int iDigit = 0;
        int iProduct = 1;

        while(iNo > 0)
        {
            iDigit = iNo % 10;
            iProduct = iProduct * iDigit;
            iNo = iNo / 10;
        }

        System.out.println("Product : " + iProduct);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Calculates product of digits of a number.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A21q1
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.productDigits(234);
    }
}
