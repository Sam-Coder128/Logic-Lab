// Program      : Sum of Even Numbers Demo
// Input        : 10
// Output       : Sum : 30
// Description  : Calculates sum of even numbers from 1 to given number
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void sumEvenNumbers(int iNo)
    {
        int iCnt = 0;
        int iSum = 0;

        for(iCnt = 1; iCnt <= iNo; iCnt++)
        {
            if(iCnt % 2 == 0)
            {
                iSum = iSum + iCnt;
            }
        }

        System.out.println("Sum : " + iSum);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates sum of even numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A20q1
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.sumEvenNumbers(10);
    }
}
