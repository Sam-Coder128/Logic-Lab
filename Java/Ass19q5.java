////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Power Calculation Demo
// Input        : Base and Exponent (integers)
// Output       : Result of base^exponent
// Methods      : calculatePower(), System.out.println()
// Description  : Calculates power of a number using loop
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void calculatePower(int iBase, int iExp)
    {
        int result = 1;
        for(int i = 1; i <= iExp; i++)
        {
            result *= iBase;
        }
        System.out.println(iBase + "^" + iExp + " = " + result);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Demonstrates power calculation.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class A19q5
{
    public static void main(String[] args)
    {
        Logic lobj = new Logic();
        lobj.calculatePower(2, 5);
    }
}
