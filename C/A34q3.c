// Output : “MMOS”

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Copy Capital Characters Demo
// Description  : Copies only capital letters from source string to destination
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void StrCpyCap(char *src, char *dest)
{
    while(*src != '\0')
    {
        if((*src >= 'A') && (*src <= 'Z'))
        {
            *dest = *src;
            dest++;
        }
        src++;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Copies capital letters from string.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30] = "Marvellous Multi OS";
    char brr[30]; // Empty string

    StrCpyCap(arr,brr);

    printf("%s",brr); // MMOS

    return 0;
}
