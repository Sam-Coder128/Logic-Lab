////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : String Copy N Characters Demo (strncpy implementation)
// Description  : Copies given number of characters from source to destination
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void StrNCpyX(char *src, char *dest, int iCnt)
{
    while((*src != '\0') && (iCnt != 0))
    {
        *dest = *src;
        src++;
        dest++;
        iCnt--;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Copies N characters from string.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30] = "Marvellous Multi OS";
    char brr[30]; // Empty string

    StrNCpyX(arr,brr,10);

    printf("%s",brr); // Marvellous

    return 0;
}
