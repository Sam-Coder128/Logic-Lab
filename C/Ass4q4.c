////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Non-Factor Display Demo
// Input        : Integer number entered by user
// Output       : Prints all non-factors of the number
// Methods      : NonFact(), printf(), scanf()
// Description  : Demonstrates identification of non-factors using for loop in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void NonFact(int iNo)
{
    int i = 0;

    for(i = 1; i < iNo; i++)
    {
        if(iNo % i != 0)
        {
            printf("%d\t", i);
        }
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays non-factors of a user-entered number.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number: ");
    scanf("%d", &iValue);

    NonFact(iValue);

    return 0;
}
