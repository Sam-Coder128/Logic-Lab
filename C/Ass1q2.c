////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Display Loop Demo
// Input        : None
// Output       : Prints "Marvellous" five times
// Methods      : Display(), printf()
// Description  : Demonstrates simple loop execution with repeated output in C.
