////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void Display(){
    int i = 0;
    for (i = 1; i <= 5; i++)     printf("Marvellous\n");
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Shows repeated printing of a string using a for loop.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()  {
    Display();
    return 0;
}
