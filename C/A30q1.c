// Program      : Display ASCII Table Demo
// Description  : Displays ASCII values from 0 to 255 with characters
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void DisplayASCII()
{
    int i = 0;

    printf("Decimal\tCharacter\n");
    for(i = 0; i <= 255; i++)
    {
        printf("%d\t%c\n", i, i);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays ASCII table.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    DisplayASCII();

    return 0;
}
