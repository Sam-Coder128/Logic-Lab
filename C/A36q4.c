// Output : 713

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Toggle 7th and 10th Bit Demo
// Description  : Accepts number and toggles 7th and 10th bits
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef unsigned int UINT;

UINT ToggleBit(UINT iNo)
{
    UINT iMask = 0x00000240;   // Mask for 7th (bit 6) and 10th (bit 9)
    return (iNo ^ iMask);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Toggles 7th and 10th bits.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%u",&iValue);

    iRet = ToggleBit(iValue);

    printf("Modified number is %u",iRet);

    return 0;
}
