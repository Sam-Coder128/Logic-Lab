// Program      : Toggle 7th Bit Demo
// Description  : Accepts number and toggles 7th bit
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef unsigned int UINT;

UINT ToggleBit(UINT iNo)
{
    UINT iMask = 0x00000040;   // Mask for 7th bit (bit 6)
    return (iNo ^ iMask);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Toggles 7th bit.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%u",&iValue);

    iRet = ToggleBit(iValue);

    printf("Modified number is %u",iRet);

    return 0;
}
