////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Star Printing Demo
// Input        : Integer number (count of stars)
// Output       : Prints stars equal to the given number
// Methods      : Display(), printf()
// Description  : Demonstrates use of for loop to print repeated characters in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void Display(int iNo) {
    int iCnt = 0;
    for (iCnt = 1; iCnt <= iNo; iCnt++)
        printf("*");
    
    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays repeated star characters using a for loop.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    iValue = 5; /* example value */
    Display(iValue);
    return 0;
}
