////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Multiple Display Demo
// Input        : Integer number entered by user
// Output       : Prints first 5 multiples of the number
// Methods      : MultipleDisplay(), printf(), scanf()
// Description  : Demonstrates multiplication table display using for loop in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Input : 4
// Output : 4 8 12 16 20

#include<stdio.h>

void MultipleDisplay(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
        printf("%d ",iNo * iCnt);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays first 5 multiples of a user-entered number.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number");
    scanf("%d",&iValue);

    MultipleDisplay(iValue);

    return 0;
}
