// Output : 1

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Off 7th and 10th Bit Demo
// Description  : Accepts number and turns OFF 7th and 10th bits if they are ON
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef unsigned int UINT;

UINT OffBit(UINT iNo)
{
    UINT iMask = 0xFFFFFB3F;   // Mask with 7th (bit 6) and 10th (bit 9) OFF
    return (iNo & iMask);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Turns OFF 7th and 10th bits.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%u",&iValue);

    iRet = OffBit(iValue);

    printf("Modified number is %u",iRet);

    return 0;
}
