////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Conditional Message Demo
// Input        : Integer number entered by user
// Output       : Prints "Hello" if number is less than 10, otherwise prints "Demo"
// Methods      : Display(), printf(), scanf()
// Description  : Demonstrates conditional execution using if-else in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void Display(int iNo)
{
    if (iNo < 10)
    {
        printf("Hello\n");
    }
    else
    {
        printf("Demo\n");
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays different messages based on user input using conditional logic.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter number: ");
    scanf("%d", &iValue);
    
    Display(iValue);
    
    return 0;
}
