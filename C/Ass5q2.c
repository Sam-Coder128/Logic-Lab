////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Display Numbers Demo
// Input        : Integer number entered by user
// Output       : Prints sequence of numbers from 1 to N
// Methods      : Display(), printf(), scanf()
// Description  : Demonstrates number display using for loop in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Input : 8
// Output : 1 2 3 4 5 6 7 8

#include<stdio.h>

void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
        printf("%d ",iCnt);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays numbers from 1 to N.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}
