////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Display Range Demo
// Input        : Integer number entered by user
// Output       : Prints numbers from -N to N
// Methods      : Display(), printf(), scanf()
// Description  : Demonstrates range display using for loop in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Input : 4
// Output : -4 -3 -2 -1 0 1 2 3 4

#include<stdio.h>

void Display(int iNo)
{
    int iCnt = 0;

    for(iCnt = -iNo; iCnt <= iNo; iCnt++)
        printf("%d ",iCnt);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays numbers from -N to N.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

