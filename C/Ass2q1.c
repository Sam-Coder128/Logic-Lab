////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Star Printing with User Input Demo
// Input        : Integer number entered by user
// Output       : Prints stars equal to the entered number
// Methods      : Display(), printf(), scanf()
// Description  : Demonstrates accepting user input and printing repeated characters in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void Display(int iNo)
{
    int iCnt = 0;
    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*");
    }
    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays user-specified number of stars using a for loop.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter number: ");
    scanf("%d", &iValue);
    
    Display(iValue);
    return 0;
}
