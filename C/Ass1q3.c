////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : While Loop Countdown Demo
// Input        : None
// Output       : Prints numbers from 5 down to 1
// Methods      : Display(), printf()
// Description  : Demonstrates countdown using while loop in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

int Display()
{
    int i = 5;
    while (i >= 1)
    {
        printf("%d\n", i);
        i--;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Clarifies use of while loop to perform reverse counting.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    return 0;
}
