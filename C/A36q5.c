// Output : 79

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : On First 4 Bits Demo
// Description  : Accepts number and turns ON first 4 bits
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef unsigned int UINT;

UINT OnBit(UINT iNo)
{
    UINT iMask = 0x0000000F;   // Mask for first 4 bits
    return (iNo | iMask);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Turns ON first 4 bits.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%u",&iValue);

    iRet = OnBit(iValue);

    printf("Modified number is %u",iRet);

    return 0;
}
