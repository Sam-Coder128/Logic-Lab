////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Off 7th Bit Demo
// Description  : Accepts number and turns OFF 7th bit if it is ON
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

typedef unsigned int UINT;

UINT OffBit(UINT iNo)
{
    UINT iMask = 0xFFFFFFBF;   // Mask with 7th bit OFF (bit position 6)
    return (iNo & iMask);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Turns OFF 7th bit.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0, iRet = 0;

    printf("Enter number : ");
    scanf("%u",&iValue);

    iRet = OffBit(iValue);

    printf("Modified number is %u",iRet);

    return 0;
}
