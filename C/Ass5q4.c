// Program      : Odd Number Display Demo
// Input        : Integer number entered by user
// Output       : Prints odd numbers up to N
// Methods      : OddDisplay(), printf(), scanf()
// Description  : Demonstrates odd number display using for loop in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

// Input : 18
// Output : 1 3 5 7 9 11 13 15 17

#include<stdio.h>

void OddDisplay(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
        if(iCnt % 2 != 0)   printf("%d ",iCnt);
    
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays odd numbers up to N.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number:");
    scanf("%d",&iValue);

    OddDisplay(iValue);

    return 0;
}

