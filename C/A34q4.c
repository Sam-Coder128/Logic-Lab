// Program      : Copy Small Characters Demo
// Description  : Copies only small letters from source string to destination
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void StrCpySmall(char *src, char *dest)
{
    while(*src != '\0')
    {
        if((*src >= 'a') && (*src <= 'z'))
        {
            *dest = *src;
            dest++;
        }
        src++;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Copies small letters from string.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30] = "Marvellous multi OS";
    char brr[30]; // Empty string

    StrCpySmall(arr,brr);

    printf("%s",brr); // arvellous multi

    return 0;
}
