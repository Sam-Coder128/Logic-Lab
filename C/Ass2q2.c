////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Star Printing with While Loop Demo
// Input        : Integer number entered by user
// Output       : Prints stars equal to the entered number
// Methods      : Display(), printf(), scanf()
// Description  : Demonstrates use of while loop to print repeated characters in C.
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>

void Display(int iNo)
{
    while (iNo > 0)
    {
        printf("*");
        iNo--;
    }
    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays user-specified number of stars using a while loop.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter number: ");
    scanf("%d", &iValue);
    
    Display(iValue);
    return 0;
}
