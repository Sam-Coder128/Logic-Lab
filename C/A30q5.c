// Output :
// Decimal      65
// Octal        0101
// Hexadecimal  0X41

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : Display Character Codes Demo
// Description  : Displays decimal, octal and hexadecimal values of character
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void Display(char ch)
{
    printf("Decimal      %d\n", ch);
    printf("Octal        %o\n", ch);
    printf("Hexadecimal  %X\n", ch);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Displays character codes.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter the character : ");
    scanf("%c",&cValue);

    Display(cValue);

    return 0;
}
