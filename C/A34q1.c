// Output : “Marvellous Multi OS” in another string

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Program      : String Copy Demo (strcpy implementation)
// Description  : Copies contents of one string into another
// Author       : Samruddh Shivkumar Birajdar
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void StrCpyX(char *src, char *dest)
{
    while(*src != '\0')
    {
        *dest = *src;
        src++;
        dest++;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application : Copies string into another.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30] = "Marvellous Multi OS";
    char brr[30]; // Empty string

    StrCpyX(arr,brr);

    printf("%s",brr); // Marvellous Multi OS

    return 0;
}
